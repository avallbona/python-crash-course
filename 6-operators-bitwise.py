"""
Bitwise operators
"""

x = 10  # 0000 1010
y = 4  # 0000 0100

# AND
# Output: x & y is 0
print("x & y is", x & y)

# OR
# Output: x | y is 14
print("x | y is", x | y)

# NOT
# Output: ~x is -11
print("~x is", ~x)

# XOR
# Output: x ^ y is 14
print("x ^ y is", x ^ y)

# RIGHT SHIFT
# Output: x>>2 is 2
print("x>>2 is ", x >> 2)

# LEFT SHIFT
# Output: x<<2 is 40
print("x<<2 is", x << 2)


# this is a comment
print("this is awesomic", x)
