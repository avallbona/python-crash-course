"""
Variables
"""

# string var
a = "this is a string var"
print(a)

# int var
b = 12
print(b)

# float var
c = 1.4
print(c)

# concatenate different type vars
print(a + str(b))


# show var type
print("type a: {}".format(type(a)))
print("type b: {}".format(type(b)))
print("type c: {}".format(type(c)))
